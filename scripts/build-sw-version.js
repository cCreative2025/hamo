#!/usr/bin/env node
/**
 * Writes public/sw-version.js with a build-time version string so the
 * service worker cache invalidates on every new build.
 *
 * Version format: "<short-git-sha>-<yyyymmddHHMM>" (falls back to timestamp
 * only if git is unavailable, e.g. in a Vercel build that lacks .git).
 */
const { execSync } = require('node:child_process');
const { writeFileSync, mkdirSync } = require('node:fs');
const { resolve, dirname } = require('node:path');

function safeGitSha() {
  try {
    return execSync('git rev-parse --short HEAD', { stdio: ['ignore', 'pipe', 'ignore'] })
      .toString()
      .trim();
  } catch {
    return null;
  }
}

function stamp() {
  const d = new Date();
  const p = (n) => String(n).padStart(2, '0');
  return `${d.getFullYear()}${p(d.getMonth() + 1)}${p(d.getDate())}${p(d.getHours())}${p(d.getMinutes())}`;
}

const sha = safeGitSha();
const version = sha ? `${sha}-${stamp()}` : stamp();
const outPath = resolve(__dirname, '..', 'public', 'sw-version.js');
mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(
  outPath,
  `// Generated by scripts/build-sw-version.js — do not edit.\nself.SW_VERSION = '${version}';\n`,
  'utf8'
);
console.log(`[sw-version] wrote ${outPath} — ${version}`);
